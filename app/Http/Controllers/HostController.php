     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $hosts = new Host();
        $hosts->nombre = $request->nombre;
        $hosts->email = $request->email;
        $hosts->telefono = $request->telefono;
        $hosts->rfc = $request->rfc;
        $hosts->razon_social = $request->razon_social;
        $hosts->es_activo = true;

        $hosts->save();
    }

